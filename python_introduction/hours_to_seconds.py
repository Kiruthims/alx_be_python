#hours_to_seconds.py

#Define the number of hours
hours = 2  # Change the number of hours as needed

#Calculate the number of seconds
seconds = hours * 3600 # One hour has 60 minutes and one minutes has 60 seconds, thus 60 * 60 = 3600 seconds

#Print the result
print(f"{hours} hour(s) is {seconds} seconds")
